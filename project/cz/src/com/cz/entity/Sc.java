package com.cz.entity;

public class Sc {
	private int scId;	//收藏id
	private int uid;	//用户id
	private int spid;	//商品id
	public int getScId() {
		return scId;
	}
	public void setScId(int scId) {
		this.scId = scId;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getSpid() {
		return spid;
	}
	public void setSpid(int spid) {
		this.spid = spid;
	}
	
	
}
